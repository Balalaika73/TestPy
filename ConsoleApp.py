# Инициализация переменной для хранения суммы
total_sum = 0

# Просим пользователя ввести количество чисел для сложения
num_count = int(input("Введите количество чисел для сложения: "))

# Цикл для получения чисел от пользователя и их сложения
for i in range(num_count):
    num = float(input("Введите число: "))
    total_sum += num

# Выводим сумму
print("Сумма введенных чисел:", total_sum)
